function angleDiff(a, b) { let d = Math.abs(a - b) % 360; return d > 180 ? 360 - d : d; }
function findAspects(planets) {
  const res = [];
  for (let i = 0; i < planets.length; i++)
    for (let j = i + 1; j < planets.length; j++) {
      const d = angleDiff(planets[i].lon, planets[j].lon);
      for (const asp of ASPECTS)
        if (Math.abs(d - asp.angle) <= asp.orb) {
          res.push({ a: planets[i], b: planets[j], asp });
          break;
        }
    }
  return res.sort((x, y) => ASPECTS.indexOf(x.asp) - ASPECTS.indexOf(y.asp));
}

