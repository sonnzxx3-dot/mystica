function moonPhase(date) {
  // алгоритм Conway, приблизительный возраст луны 0..29.53
  const y = date.getFullYear(), m = date.getMonth() + 1, d = date.getDate();
  let r = y % 100; r %= 19; if (r > 9) r -= 19;
  r = ((r * 11) % 30) + (m < 3 ? m + 12 : m) + d;
  if (m < 3) r += 0;
  r -= 8.3; r = Math.floor(r + 0.5) % 30; const age = (r < 0 ? r + 30 : r);
  const idx = Math.floor((age / 29.53) * 8) % 8;
  return { ...MOON_PHASES[idx], age: Math.round(age) };
}

