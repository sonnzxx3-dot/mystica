function localSiderealTime(JD, lonEast) {
  const T = (JD - 2451545.0) / 36525.0;
  let gmst = 280.46061837 + 360.98564736629 * (JD - 2451545.0) + 0.000387933 * T * T - T * T * T / 38710000.0;
  return NORM(NORM(gmst) + lonEast);
}
function computeAscendant(year, month, day, hour, lat, lonEast) {
  const JD = julianDay(year, month, day, hour);
  const eps = 23.4393 * D2R;
  const ramc = localSiderealTime(JD, lonEast) * D2R;
  const latR = lat * D2R;
  let asc = Math.atan2(Math.cos(ramc), -(Math.sin(ramc) * Math.cos(eps) + Math.tan(latR) * Math.sin(eps)));
  return NORM(asc * R2D);
}
// равнодомная система: дома по 30° от асцендента
function computeHouses(ascLon) {
  return Array.from({ length: 12 }, (_, i) => {
    const cusp = NORM(ascLon + i * 30);
    const si = Math.floor(cusp / 30);
    return { house: i + 1, sign: SIGN_NAMES[si], signSym: SIGN_SYM[si], meaning: HOUSE_MEANINGS[i], cusp };
  });
}
// в каком доме находится планета (равнодомная)
function houseOfPlanet(planetLon, ascLon) {
  return Math.floor(NORM(planetLon - ascLon) / 30) + 1;
}



